function convertToCLB(score, ability, testType) {
    if (!score) return 0;
    const s = parseFloat(score);
    if (testType === 'CELPIP') {
        if (s >= 10) return 10;
        if (s >= 9) return 9;
        if (s >= 8) return 8;
        if (s >= 7) return 7;
        if (s >= 6) return 6;
        if (s >= 5) return 5;
        if (s >= 4) return 4;
        return 0;
    }
    const table = ieltsToCLB[ability];
    if (!table) return 0;
    for (const [threshold, clb] of table) {
        if (s >= threshold) return clb;
    }
    return 0;
}

